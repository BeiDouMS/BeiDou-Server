var status = -1;

function start(mode, type, selection) {
    status++;
    if (mode != 1) {
        if (type == 1 && mode == 0) {
            status -= 2;
        } else {
            qm.sendOk("这应该并不难啊？如果你改变了主意，可以再来找我。");
            qm.dispose();
            return;
        }
    }
    if (status == 0) {
        qm.sendAcceptDecline("啊！差点忘了！看我的记性。请你把做好的#b#t4032389##k交给#b#p1300004##k，并向他报告。");
    } else if (status == 1) {
        qm.forceStartQuest();
        qm.gainItem(4032389, 1);
        qm.sendOk("#b魔法大臣#k让我制作好#b奇拉蘑菇孢子#k之后，给他送一份样本过去。这是样本，请你把它交给#b魔法大臣#k。");
    } else if (status == 2) {
        qm.dispose();
    }
}

function end(mode, type, selection) {
    status++;
    if (mode != 1) {
        if (type == 1 && mode == 0) {
            status -= 2;
        } else {
            qm.dispose();
            return;
        }
    }
    if (status == 0) {
        qm.sendOk("#b奇拉蘑菇孢子#k做好了吗？");
    } else if (status == 1) {
        qm.forceCompleteQuest();
        qm.gainExp(4200);
        qm.gainItem(4032389, -1);
        qm.sendOk("嗯……这就是#b奇拉蘑菇孢子#k啊。谢谢你。帮我谢谢#b斯卡斯#k。");
    } else if (status == 2) {
        qm.dispose();
    }
}
	
