/*
	Incomplete Magic Square
 */

var status;

function start() {
    status = -1;
    action(1, 0, 0);
}

function action(mode, type, selection) {
    if (mode == -1) {
        cm.dispose();
    } else {
        if (mode == 0 && status == 0) {
            cm.dispose();
            return;
        }
        if (mode == 1) {
            status++;
        } else {
            status--;
        }

        if (status == 0) {
            cm.sendAcceptDecline("这个魔法五角星还没有完成。您想完成这个魔法五角星的绘制吗？");
            
        } else if (status == 1) {
            cm.weakenAreaBoss(8090000, "魔法五角星已经完成。用于消除迪特（Deet）和罗伊（Roi）的咒语已经被启动了。");
			cm.dispose();
        }
    }
}
