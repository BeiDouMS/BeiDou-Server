function action(mode, type, selection) 
{
	if (CheckStatus(mode))
	{
	    if (status == 0)
	    {
			//第一层对话
			title = "#fMap/MapHelper/weather/rose/0#整理083UI如下#fMap/MapHelper/weather/rose/0#\r\n#b";
			for ( i = 0; i < 7; i++)
			{
				title += "#L" + i + "#" + co[i][0] + "#l" + "\t";
                if (i%3 == 0)
                    title +="\r\n";				
			}
			cm.sendSimple(title + "#k\r\n----------------------------------------------------------------------------");
	    }
		else if (status == 1 )
		{
			//第二层对话
			var iCount = parseInt(co[selection][1]);
			text = "查询到的UI如下，请过目：\r\n\r\n";
			for (i = 0; i < iCount; i++)
			{
				text += "#L" + i + "#" + UI_Total[selection][i][1] + "#l";
				if (i != iCount - 1)
				{
					text += ",";
				}
			}
			target = selection;
			cm.sendSimple(text);
		}
		else
		{
			//最后一层对话完继续循环至此，退出结束
			cm.sendOk("查询到的编号为：" + "#r" + UI_Total[target][selection][0] + "#b-需要就复制拿出来用\r\n\r\n图示：#k" + UI_Total[target][selection][1]);
			cm.dispose();
		}
	}
			
}

function CheckStatus(mode)
{
	if (mode == -1)
	{
		cm.dispose();
		return false;
	}
	
	if (mode == 1)
	{
		status++;
	}
	else
	{
		status--;
	}
	
	if (status == -1)
	{
		cm.dispose();
		return false;
	}	
	return true;
}